package operaciones;

public class Operaciones {

    public static int sumar(int a, int b) {
        System.out.println("Metodo Enteros");
        return a + b;

    }

    //Puede variar el tipo de retorno
    public static double sumar(double a, double b) {
        System.out.println("Metodo Double");
        return a + b;
    }
}
